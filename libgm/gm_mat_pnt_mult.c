#include "include/libgm.h"

t_float4	gm_mat_pnt_mult(t_float4 V, t_mat4 T)
{
	int			i;
	t_float4 	p;

	i = -1;
	while (++i < 4)
		p.v[i] = V.v[0] * T.v[i].v[0] + V.v[1] * T.v[i].v[1]
				+ V.v[2] * T.v[i].v[2] + V.v[3] * T.v[i].v[3];
	return (p);
}
